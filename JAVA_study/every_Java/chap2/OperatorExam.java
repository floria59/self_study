package chap2;

public class OperatorExam {

	public static void main(String[] args) {
		int i1 = -5;
        System.out.println(i1);
        
        int i2 = ++i1;
        System.out.println(i2);
        
        int i3 = ++i1;
        System.out.println(i3);
        
        int i4 = i1++;
        System.out.println(i4);
        
        int i5 = i1++;
        System.out.println(i5);
        
        int i = 5;
        int j = 2;
        
        System.out.println(i / (double) j);
        
        
	}

}
